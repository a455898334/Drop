$("div").hide();
var newpage = true;
$("#drop").click(
	function(){
		if(newpage){
			$("#tbl").animate({top:'-8px'});
			newpage=false;
		}
		$("div").hide();
		$("#new").show();
	}
);
$("#browse").click(
	function(){
		if(newpage){
			$("#tbl").animate({top:'-8px'});
			newpage=false;
		}
		$("div").hide();
		$("#scan").show();
	}
);
